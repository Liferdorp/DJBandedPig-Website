    <div class="biografie">
      <br/>
      <p>Sanne van Gend beter bekend als DJ BandedPig is geboren op 22 februari 1996 in Willemstad. Vanaf 
      kinds af aan luisterde zij al veel muziek. Ook heeft zij regelmatig meegedaan aan talentenjachten 
      in de wijk en was zij lid van de lokale musical vereniging.</p> <p> Begin 2014 besloot Sanne om een 
      professionele dj set aan te schaffen. Ze heeft deze dj set aangeschaft, omdat zij erg enthousiast was 
      geworden over de dance muziek tijdens feesten als “dancetour” en “HBO intro festival”.</p><p>
      Op 26 mei 2015 besloot zij om zich aan te melden voor de Battle of the dj’s van het HBO intro festival in Den Bosch. 
      Ook stond Sanne 26 september 2015 op Festival Ontpop in Klundert. 
      In 2016 heeft Sanne meegedaan aan Beat the DJ in Halsteren, wat mede georganiseerd wordt door Gebouw-T in Bergen op Zoom.</p><p> 
      Op dit moment studeert Sanne op het Avans in Breda en probeert zij in 
      haar vrije tijd, bezig te zijn met muziek maken en het door elkaar mixen van de muziek. Sanne haar 
      persoonlijke voorkeuren liggen bij de electro en deep house, maar eigenlijk is zij een echte 
      allrounder.</p>
